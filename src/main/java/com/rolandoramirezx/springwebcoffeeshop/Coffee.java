package com.rolandoramirezx.springwebcoffeeshop;

public class Coffee {

    long id;
    String description;

    public void setId(long id){
        this.id = id;
    }

    public void setDescription(String description){
        this.description = description;
    }
    public long getId() {
        return id;
    }

    public String getDescription(){
        return description;
    }


}






